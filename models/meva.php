        function getAll($ope) {
            $idcal = $this->getIdcal();
            $fecini = $this->getFecini();
            $fecfin = $this->getFecfin();
            $nota = $this->getNota();
            
            $tipos = ['jef', 'par', 'sub', 'aut'];
            $campos = [];
            $preg = [];

            foreach ($tipos as $tipo) {
                for ($i = 1; $i <= 25; $i++) $campos[] = "r$tipo.res$i AS r{$tipo}$i";
            }
            $respuestas = implode(", ", $campos);
            
            for ($i = 1; $i <= 25; $i++) $preg[] = "f.pre$i";
            $preguntas = implode(", ", $preg);

            $sql = "SELECT c.idcal, c.idper, c.feccal, c.nota, c.rutpdf, c.idevasub, v.nomval AS tfor, CONCAT(pe.nomper, ' ', pe.apeper) AS eva, pe.cargo AS ceva, ea.feceva AS feva, CONCAT(pj.nomper, ' ', pj.apeper) AS jef, pj.cargo AS cjef, ej.feceva AS fjeva, CONCAT(pp.nomper, ' ', pp.apeper) AS par, pp.cargo AS cpar, ep.feceva AS fpeva, CONCAT(ps.nomper, ' ', ps.apeper) AS sub, ps.cargo AS csub, es.feceva AS fseva, f.codfor, f.verfor, f.fecfor, f.nomsec1, f.nomsec2, f.nomsec3, f.nomsec4, f.nomsec5, f.porjef, f.porpar, f.poraut, f.porsub, $preguntas, $respuestas FROM calificacion AS c INNER JOIN persona AS pe ON c.idper=pe.idper INNER JOIN valor AS v ON pe.idvfor=v.idval INNER JOIN evaluacion AS ej ON c.idevajef=ej.ideva INNER JOIN persona AS pj ON ej.idpereval=pj.idper INNER JOIN evaluacion AS ep ON c.idevapar=ep.ideva INNER JOIN persona AS pp ON ep.idpereval=pp.idper INNER JOIN evaluacion AS ea ON c.idevaaut=ea.ideva LEFT JOIN evaluacion AS es ON c.idevasub=es.ideva LEFT JOIN persona AS ps ON es.idpereval=ps.idper INNER JOIN respuesta AS rjef ON ej.ideva=rjef.ideva INNER JOIN respuesta AS rpar ON ep.ideva=rpar.ideva LEFT JOIN respuesta AS rsub ON es.ideva=rsub.ideva INNER JOIN respuesta AS raut ON ea.ideva=raut.ideva INNER JOIN formato AS f ON pe.idfor=f.idfor";
            if($ope=="one") $sql .= " WHERE c.idcal=:idcal";
            if($ope=="bus"){
                $sql .= " WHERE c.feccal!=''";
                if($fecini) $sql .= " AND DATE(c.feccal)>=:fecini";
		        if($fecfin) $sql .= " AND DATE(c.feccal)<=:fecfin";
		        if($nota) $sql .= " AND c.nota >=:nota";
            }
            $modelo = new conexion();
            $conexion = $modelo->get_conexion();
            $result = $conexion->prepare($sql);
            if($ope=="one") $result->bindParam(":idcal", $idcal);
            if($ope=="bus"){
                if($fecini) $result->bindParam(":fecini", $fecini);
		        if($fecfin) $result->bindParam(":fecfin", $fecfin);
		        if($nota) $result->bindParam(":nota", $nota);
            }
            $result->execute();
            $res = $result-> fetchall(PDO::FETCH_ASSOC);
            return $res;
        }

